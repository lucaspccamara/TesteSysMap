using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace Api_SysMap.Models
{
    public class Number
    {
        public int sum(int a, int b)
        {
            return a + b;
        }

        public float divide(int a, int b)
        {
            return (float)a / (float)b;
        }
    }
}
